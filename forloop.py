
# for loop with range
for i in range(5):
    print(i)


# for loop with list
fruits = ["apple", "banana", "cherry"]
for f in fruits:
    print(f)

# for loop with string
name = "nishita"
for char in name:
    print(char)

# for loop with dictionary
student = {"name": "nishita" , "age" : 21 , "city" : "delhi"}
for key in student:
    print(key ,":", student[key])    

     
                

